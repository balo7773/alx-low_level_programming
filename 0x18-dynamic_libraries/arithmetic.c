#include <stdlib.h>

/**
 * add - func to add
 * @a: int datatype
 * @b: int datatype
 * Return: add
*/
int add(int a, int b)
{
    return (a + b);
}
/**
 * add - func to sub
 * @a: int datatype
 * @b: int datatype
 * Return: sub
*/
int sub(int a, int b)
{
    return (a - b);
}
/**
 * mod - func to mod
 * @a: int datatype
 * @b: int datatype
 * Return: mod
*/
int mod(int a, int b)
{
    return (a % b);
}
/**
 * mul - func to add
 * @a: int datatype
 * @b: int datatype
 * Return: mul
*/
int mul(int a, int b)
{
    return (a * b);
}